package org.cookbookgeeks.webkochbuch.domain;

import java.io.File;
import java.io.Serializable;
import java.nio.file.Path;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

/**
 * POJO that represents an image.
 * 
 * @author Nils Sommer
 *
 */
@Entity
@Table(name="t_images")
public class Image implements Serializable {

	private static final long serialVersionUID = 4364318268080958100L; // generated by eclipse
	
	@Id
	@Column(name="id")
	@GeneratedValue(strategy=GenerationType.SEQUENCE, generator="my_entity_seq_gen")
	@SequenceGenerator(name="my_entity_seq_gen", sequenceName="HIBERNATE_SEQUENCE", allocationSize=1)
	private Long id;
	
	@Column(name="file_path")
	private String path;
	
	@Column(name="description")
	private String description;
	
	@ManyToOne
	@JoinColumn(name = "user_id")
	private User user;
	
	@ManyToOne
	@JoinColumn(name="recipe_id")
	private Recipe recipe;
	
	@Column(name = "creation_date")
	private Date creation;
	
	@Column(name = "last_edit")
	private Date modification;
	
	/**
	 * Default Constructor.
	 */
	public Image() {
		
	}
	
	/**
	 * Constructor which sets the path and the description of the image.
	 * 
	 * @param path the path of the image file
	 * @param description the description of the image
	 */
	public Image(String path, String description) {
		this.path = path;
		this.description = description;
	}
	
	/**
	 * Constructor which sets the recipe, the path and the description of the image.
	 * 
	 * @param recipe the recipe the image belongs to
	 * @param path the path of the image file
	 * @param description the description of the image
	 */
	public Image(Recipe recipe, String path, String description) {
		this.recipe = recipe;
		this.path = path;
		this.description = description;
	}
	
	// ----- Getters and setters here: -----
	
	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}
	
	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the filePath
	 */
	public String getPath() {
		return path;
	}

	/**
	 * @param file Uploaded file
	 * @param uploadFolder Upload folder where file ist stored
	 */
	public void setPath(File file, File uploadFolder) {
		// Relativize path to file, depending on where the upload folder is.
		Path filePath = file.toPath();
		Path folderPath = uploadFolder.toPath();
		String relativizedPath = folderPath.relativize(filePath).toString();
		// For microsoft operating systems: Convert path separator characters with url separator characters.
		this.path = relativizedPath.replaceAll("\\\\","/");
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	
	/**
	 * @return the user
	 */
	public User getUser() {
		return user;
	}
	
	/**
	 * @param user the user to set
	 */
	public void setUser(User user) {
		this.user = user;
	}
	
	/**
	 * @return the recipe
	 */
	public Recipe getRecipe() {
		return recipe;
	}
	
	/**
	 * @param recipe the recipe to set
	 */
	public void setRecipe(Recipe recipe) {
		this.recipe = recipe;
	}
	
	/**
	 * @return the creation
	 */
	public Date getCreation() {
		return creation;
	}

	/**
	 * @param creation the creation to set
	 */
	public void setCreation(Date creation) {
		this.creation = creation;
	}

	/**
	 * @return the modification
	 */
	public Date getModification() {
		return modification;
	}

	/**
	 * @param modification the modification to set
	 */
	public void setModification(Date modification) {
		this.modification = modification;
	}
	
	// ----- other methods -----
	
	/**
	 * @return a host relative url that can be referenced for GET requests in a view.
	 */
	public String getViewUrl() {
		return "/images/" + this.path;
	}
	
	/** {@inheritDoc} */
	public String toString() {
		return "File path: " + path + "\nDescription: " + description +
				"\nId: " + id;
	}

}
